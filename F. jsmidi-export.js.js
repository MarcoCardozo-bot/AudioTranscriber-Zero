function midiExport(notes){
  const uint8=new Uint8Array([
    0x4d,0x54,0x68,0x64,0x00,0x00,0x00,0x06,0x00,0x00,0x00,0x01,0x00,0x60, // header
    0x4d,0x54,0x72,0x6b,0x00,0x00,0x00,0x0c+notes.length*7,0x00,0xc0,0x00 // track
  ]);
  // aqui entra logica de var-delta tempo; simplifiquei
  return uint8;
}
